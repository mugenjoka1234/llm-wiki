def find_unacknowledged_digests(pages: dict[str, dict]) -> list[tuple[str, str]]:
    """Return (entity_slug, digest_slug) pairs where a digest's `related:` names an
    entity page but that entity page's `sources:` does not list the digest back.

    This enforces the compounding contract: digest → entity (via related:) must be
    mirrored by entity → digest (via sources:). Broken pairs mean the entity page was
    not enriched after the digest was ingested.

    Only checks digests (type: source) against non-source, non-synthesis entity pages.
    """
    missing: list[tuple[str, str]] = []
    for slug, page in pages.items():
        fm = page.get("fm", {})
        if fm.get("type") != "source":
            continue
        digest_tail = slug.rsplit("/", 1)[-1]
        related = fm.get("related") or []
        for ref in related:
            ref_slug = _unwrap_item(str(ref))
            # Find matching entity page (exact slug match or tail match)
            for entity_slug, entity_page in pages.items():
                etype = entity_page.get("fm", {}).get("type")
                if etype in ("source", "synthesis"):
                    continue
                entity_tail = entity_slug.rsplit("/", 1)[-1]
                if entity_slug != ref_slug and entity_tail != ref_slug:
                    continue
                # Found the entity page — check its sources: list
                entity_sources = entity_page.get("fm", {}).get("sources") or []
                entity_source_slugs = {_unwrap_item(str(s)) for s in entity_sources}
                if slug not in entity_source_slugs and digest_tail not in entity_source_slugs:
                    missing.append((entity_slug, slug))
                break
    return sorted(missing)


def validate_external_refs(pages: dict[str, dict], wiki_root: Path) -> list[str]:
    """Check that external-ref fields point to resolvable targets.

    Returns warning strings (not errors) — broken cross-wiki refs are exit 1, not exit 2.
    Supports two URI formats:
    - wiki://domain/slug  →  resolved via resolve_wiki.py resolve-uri
    - /absolute/path/to/file.md or ~/...  →  checked via Path.exists()
    """
    import subprocess

    warnings: list[str] = []

    # Locate resolve_wiki.py: try <wiki_root>/../plugin/scripts/ first, then CLAUDE_PLUGIN_ROOT env
    resolve_script = wiki_root.parent / "plugin" / "scripts" / "resolve_wiki.py"
    if not resolve_script.exists():
        plugin_root = os.environ.get("CLAUDE_PLUGIN_ROOT", "")
        if plugin_root:
            resolve_script = Path(plugin_root) / "scripts" / "resolve_wiki.py"

    for slug, page in pages.items():
        ext_ref = page.get("fm", {}).get("external-ref", "")
        if not ext_ref or ext_ref == '""' or ext_ref.strip('"') == "":
            continue
        ext_ref = ext_ref.strip('"').strip("'")

        if ext_ref.startswith("wiki://"):
            if not resolve_script.exists():
                warnings.append(
                    f"{slug}: external-ref '{ext_ref}' cannot be validated "
                    f"— resolve_wiki.py not found"
                )
                continue
            result = subprocess.run(
                ["python3", str(resolve_script), "resolve-uri", ext_ref],
                capture_output=True, text=True
            )
            if result.returncode != 0:
                warnings.append(
                    f"{slug}: external-ref '{ext_ref}' cannot be resolved "
                    f"— domain not in registry"
                )
            else:
                target = Path(result.stdout.strip())
                if not target.exists():
                    warnings.append(
                        f"{slug}: external-ref '{ext_ref}' resolves to "
                        f"'{target}' but file not found"
                    )
        elif ext_ref.startswith("/") or ext_ref.startswith("~"):
            target = Path(ext_ref.replace("~", str(Path.home())))
            if not target.exists():
                warnings.append(f"{slug}: external-ref '{ext_ref}' not found")

    return warnings


_BUDGET_ENTITY_PAGE = 12_000
_RESEARCH_PREFIXES = ("research-", "synthesis-", "validation-")


def check_evolution_triggers(pages: dict[str, dict], wiki_root: Path) -> list[str]:
    """Return warning strings for evolution triggers.

    Checks:
    1. Entity or question pages over _BUDGET_ENTITY_PAGE chars
    2. Research/digest files in wiki/ root (not in wiki/digests/)
    """
    warnings: list[str] = []
    wiki_dir = wiki_root / "wiki"
    for slug, page in pages.items():
        fm = page.get("fm", {})
        ptype = fm.get("type", "")
        if ptype in ("synthesis", "source"):
            continue
        page_path = wiki_dir / page["path"]
        if page_path.exists():
            char_count = page_path.stat().st_size
            if char_count > _BUDGET_ENTITY_PAGE:
                warnings.append(
                    f"{page['path']}: oversize ({char_count:,} chars, budget {_BUDGET_ENTITY_PAGE:,}) "
                    f"— consider splitting or archiving older sections"
                )
    # Check for misplaced research files in wiki/ root (top-level only, not subdirs)
    for md in wiki_dir.glob("*.md"):
        rel = md.relative_to(wiki_dir)
        if rel.parts[0].startswith("_"):
            continue
        name = md.stem
        if any(name.startswith(p) for p in _RESEARCH_PREFIXES):
            warnings.append(
                f"wiki/{name}.md: looks like a digest/research file — "
                f"move to wiki/digests/ and set type: source"
            )
            continue
        # Also flag type: source pages not in digests/
        try:
            text = md.read_text(errors="ignore")
            if text.startswith("---") and "\ntype: source\n" in text[:200]:
                warnings.append(
                    f"wiki/{name}.md: type: source found in wiki/ root — "
                    f"move to wiki/digests/"
                )
        except OSError:
            pass
    return warnings


def find_orphans(pages: dict[str, dict]) -> list[str]:
    """Return slugs with zero inbound references.

    Inbound = either a [[wikilink]] in another page's body OR a slug in another
    page's frontmatter `sources`/`related` lists. Obsidian's shortest-path
    resolution is approximated: a reference `[[tail]]` matches any slug whose
    final path component is `tail` (e.g. `[[2026-Q2]]` matches `log/2026-Q2`).

    Pages with type in {source, synthesis} are excluded:
    - source: digests legitimately have no inbound
    - synthesis: index/overview/glossary/people/log are navigational, not entities
    """
    referenced: set[str] = set()
    for slug, page in pages.items():
        body = page.get("body", "")
        for m in WIKILINK_RE.finditer(body):
            referenced.add(m.group(1))
        for list_field in ("sources", "related"):
            for ref in page.get("fm", {}).get(list_field, []) or []:
                referenced.add(ref)

    # Approximate Obsidian's shortest-path wikilink resolution: a reference to
    # just the tail (e.g. "2026-Q2") should count as an inbound for any slug
    # ending with "/2026-Q2".
    orphans = []
    for slug, page in pages.items():
        if page.get("fm", {}).get("type") in ("source", "synthesis"):
            continue
        tail = slug.rsplit("/", 1)[-1]
        if slug in referenced or tail in referenced:
            continue
        orphans.append(slug)
    return sorted(orphans)


PII_PATTERNS = {
    "email":      re.compile(r"\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}\b"),
    # phone_us requires MANDATORY separators between the three digit groups to avoid
    # false positives on order IDs like "order-6501234567" that contain embedded 10-digit
    # runs. Bare 10-digit phone numbers without separators are handled by the blocklist
    # path, not the regex.
    "phone_us":   re.compile(r"\b(?:\+?1[-.\s])?\(?\d{3}\)?[-.\s]\d{3}[-.\s]\d{4}\b"),
    "phone_intl": re.compile(r"\+\d{1,3}[-.\s]\d{3,14}"),
    "ssn":        re.compile(r"\b\d{3}-\d{2}-\d{4}\b"),
    "url_pii":    re.compile(r"https?://\S+?[?&](?:email|phone|token|user)=\S+"),
}


def scan_pii(content: str, blocklist: set[str]) -> list[dict]:
    """Return a list of matches. Each match: {pattern, match_text, start, end}."""
    matches: list[dict] = []
    for name, pat in PII_PATTERNS.items():
        for m in pat.finditer(content):
            matches.append({
                "pattern": name,
                "match_text": m.group(0),
                "start": m.start(),
                "end": m.end(),
            })
    # Case-insensitive blocklist matching — LLM stage-1 scrubs may produce
    # inconsistent casing (e.g. "sarah johnson" vs "Sarah Johnson").
    for entry in blocklist:
        if not entry:
            continue
        pat = re.compile(re.escape(entry), re.IGNORECASE)
        for m in pat.finditer(content):
            matches.append({
                "pattern": "blocklist",
                "match_text": m.group(0),
                "start": m.start(),
                "end": m.end(),
            })
    return matches


def load_blocklist(wiki_root: Path) -> set[str]:
    """Load the PII blocklist if it exists; empty set if not."""
    bl_path = wiki_root / "wiki" / "_pii" / "blocklist.txt"
    if not bl_path.exists():
        return set()
    return {line.strip() for line in bl_path.read_text().splitlines() if line.strip() and not line.startswith("#")}


def check_content_cmd(content_path: Path, wiki_root: Path) -> int:
    """Gate: scan a candidate content file. Exit non-zero if any PII found."""
    if not content_path.exists():
        print(f"error: {content_path} not found", file=sys.stderr)
        return 2
    text = content_path.read_text()
    blocklist = load_blocklist(wiki_root)
    matches = scan_pii(text, blocklist)
    if not matches:
        print(f"OK: no PII detected in {content_path}")
        return 0
    print(f"PII detected in {content_path}:")
    for m in matches:
        print(f"  [{m['pattern']}] {m['match_text']}")
    return 1


