FRONTMATTER_RE = re.compile(r"^---\s*\n(.*?)\n---\s*\n(.*)\Z", re.DOTALL)
WIKILINK_RE = re.compile(r"\[\[([^\]]+)\]\]")


def _unwrap_item(item: str) -> str:
    """Strip optional quotes and extract wikilink slug if the item is a wikilink."""
    item = item.strip()
    if len(item) >= 2 and item[0] == item[-1] and item[0] in ('"', "'"):
        item = item[1:-1]
    m = WIKILINK_RE.match(item)
    if m:
        return m.group(1)
    return item


def parse_frontmatter(text: str) -> tuple[dict, str]:
    """Return (frontmatter_dict, body_text).

    Minimal YAML-ish parser — supports the subset we use:
    - `key: value` (string scalar)
    - `key: [item1, item2]` (inline list) or `key: []` (empty list)
    - Multi-line YAML list:
          key:
            - "[[slug]]"
            - plain-item
    - Wikilinks `[[slug]]` in list values are returned as plain slugs.

    This parser is intentionally limited to the frontmatter subset the schema
    uses. It's NOT a full YAML parser. The canonical format for wikilink lists
    is now multi-line (valid YAML; Obsidian Dataview parses it correctly).

    If no frontmatter, returns ({}, text).
    """
    m = FRONTMATTER_RE.match(text)
    if not m:
        return {}, text
    fm_text, body = m.group(1), m.group(2)
    fm: dict = {}
    lines = fm_text.splitlines()
    i = 0
    while i < len(lines):
        raw = lines[i]
        line = raw.rstrip()
        if not line or line.lstrip().startswith("#"):
            i += 1
            continue
        if ":" not in line:
            i += 1
            continue
        key, _, val = line.partition(":")
        key = key.strip()
        val = val.strip()

        # Multi-line list: `key:` with no value, followed by indented `- item` lines.
        if val == "":
            items: list = []
            j = i + 1
            while j < len(lines):
                nxt = lines[j]
                # List item must be indented and start with "- "
                if (nxt.startswith(" ") or nxt.startswith("\t")) and nxt.lstrip().startswith("- "):
                    item_text = nxt.lstrip()[2:].rstrip()
                    items.append(_unwrap_item(item_text))
                    j += 1
                elif nxt.strip() == "":
                    # Blank line inside a list — tolerate but don't consume
                    break
                else:
                    break
            if items:
                fm[key] = items
                i = j
                continue
            # `key:` with nothing after it → empty string scalar
            fm[key] = ""
            i += 1
            continue

        # Inline list [a, b] or []
        if val.startswith("[") and val.endswith("]"):
            items_str = val[1:-1].strip()
            if not items_str:
                fm[key] = []
            else:
                parts = [x.strip() for x in items_str.split(",")]
                fm[key] = [_unwrap_item(p) for p in parts]
            i += 1
            continue

        # Scalar
        fm[key] = val
        i += 1
    return fm, body


