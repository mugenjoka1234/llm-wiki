DATE_RE = re.compile(r"^\d{4}-\d{2}-\d{2}$")
QUARTER_RE = re.compile(r"^\d{4}-Q[1-4]$")


# --- Numeric-claim citation gate (merge Phase 2) ---------------------------
# A paragraph is COVERED if it carries any source tag from the taxonomy, a
# wikilink, a URL, or the explicit suppression marker %%[no-claim]%% (Obsidian
# comment syntax — invisible in reading view, greppable in source).
CLAIM_COVERAGE_RE = re.compile(
    r"\[(?:internal|external)::[a-z-]+\]"
    r"|\[hypothesis[^\]]*\]"
    r"|\[verified[:\s\]]"
    r"|\[REFUTED"
    r"|%%\[no-claim\]%%"
    r"|\[\["
    r"|https?://"
)
# Numbers that never count as claims on their own.
_CLAIM_ALLOWLIST = [
    re.compile(r"\b\d{4}-\d{2}-\d{2}\b"),   # dates
    re.compile(r"\b\d{4}-Q[1-4]\b"),        # quarters
    re.compile(r"\bv\d+(?:\.\d+)*\b"),      # versions
    re.compile(r"\b(?:19|20)\d{2}\b"),      # bare years
]
# What counts as a numeric claim: currency, percentages, magnitudes,
# multipliers, standalone integers >= 3 digits. Bare 1-2 digit integers are
# deliberately NOT claims ("3 options") — too noisy.
CLAIM_NUM_RE = re.compile(
    r"[$€£]\s?\d[\d,]*(?:\.\d+)?"
    r"|\b\d[\d,]*(?:\.\d+)?\s?%"
    r"|\b\d[\d,]*(?:\.\d+)?\s?(?:[KMB]\b|million\b|billion\b|thousand\b)"
    r"|\b\d+(?:\.\d+)?x\b"
    r"|\b\d{3,}(?:,\d{3})*\b"
)
_CLAIM_EXEMPT_TYPES = {"synthesis", "question"}


# --- Trust markup (merge Phase 2) -------------------------------------------
TRUST_VERIFIED_RE = re.compile(r"\[verified:?\s*(\d{4}-\d{2}-\d{2})?\]")
TRUST_HYPOTHESIS_RE = re.compile(r"\[hypothesis[^\]]*\]")
TRUST_REFUTED_RE = re.compile(r"\[REFUTED[^\]]*\]")


def trust_counts(body: str) -> dict:
    """Count claim-status markers in a page body."""
    verified = TRUST_VERIFIED_RE.findall(body)
    dates = sorted(d for d in verified if d)
    return {
        "verified": len(verified),
        "hypothesis": len(TRUST_HYPOTHESIS_RE.findall(body)),
        "refuted": len(TRUST_REFUTED_RE.findall(body)),
        "last_pass": dates[-1] if dates else "",
    }


def render_trust_line(counts: dict) -> str:
    line = (
        f"> Trust: {counts['verified']} verified · "
        f"{counts['hypothesis']} hypothesis · {counts['refuted']} refuted"
    )
    if counts["last_pass"]:
        line += f" — last verification pass {counts['last_pass']}"
    return line


def _find_real_trust_line(lines: list[str]) -> int | None:
    """Return the index of the first NON-fenced line starting '> Trust:'.

    Fence-aware: lines inside ``` code blocks are never matched, so a fenced
    example containing '> Trust: ...' (e.g. in docs) is left alone.
    """
    in_fence = False
    for i, line in enumerate(lines):
        if line.strip().startswith("```"):
            in_fence = not in_fence
            continue
        if in_fence:
            continue
        if line.startswith("> Trust:"):
            return i
    return None


def upsert_trust_line(md_path: Path) -> bool:
    """Insert, update, or remove the human-visible trust line under the first H1.

    - When total markers > 0: insert or update the trust line.
    - When total markers == 0 AND trust line exists: remove it (plus one adjacent blank if needed).
    - When total markers == 0 AND no trust line: no-op (return False).
    Returns True if the file changed.

    Matching is fence-aware and line-by-line (not a whole-file regex): a fenced
    code example containing a '> Trust: ...' line is never treated as the real
    trust line, so it's neither rewritten nor mistaken for the real one.
    """
    text = md_path.read_text()
    m = FRONTMATTER_RE.match(text)
    body = m.group(2) if m else text
    counts = trust_counts(body)
    total = counts["verified"] + counts["hypothesis"] + counts["refuted"]

    lines = text.splitlines()
    trailing_newline = "\n" if text.endswith("\n") else ""

    # Case 1: markers present — insert or update trust line
    if total > 0:
        new_line = render_trust_line(counts)
        ti = _find_real_trust_line(lines)
        if ti is not None:
            # Update existing trust line
            if lines[ti] == new_line:
                return False
            lines[ti] = new_line
            new_text = "\n".join(lines) + trailing_newline
        else:
            # Insert new trust line after H1
            h1 = next((i for i, l in enumerate(lines) if l.startswith("# ")), None)
            if h1 is None:
                return False
            # Insert blank line before trust line, then the trust line itself
            lines.insert(h1 + 1, "")
            lines.insert(h1 + 2, new_line)
            # Ensure blank line after trust line if next line is non-blank
            if h1 + 3 < len(lines) and lines[h1 + 3].strip():
                lines.insert(h1 + 3, "")
            new_text = "\n".join(lines) + trailing_newline
        if new_text != text:
            md_path.write_text(new_text)
            return True
        return False

    # Case 2: zero markers — remove stale trust line if present
    ti = _find_real_trust_line(lines)
    if ti is not None:
        # Remove the trust line
        lines.pop(ti)
        # If removal would leave two consecutive blank lines, remove one
        if ti < len(lines) and ti > 0 and lines[ti - 1].strip() == "" and lines[ti].strip() == "":
            lines.pop(ti)
        new_text = "\n".join(lines) + trailing_newline
        if new_text != text:
            md_path.write_text(new_text)
            return True
    return False


# --- Readability checks (merge Phase 2) --------------------------------------
_NAV_SLUGS = {"index", "overview", "glossary", "people"}
_BOTTOM_LINE_TYPES = {"initiative", "segment", "synthesis"}
_SECTION_MIN_CHARS = 40


def _section_content(body: str, heading: str) -> str:
    """Text of a `## heading` section with HTML comments stripped; '' if absent."""
    m = re.search(
        rf"^## {re.escape(heading)}\s*$(.*?)(?=^## |\Z)",
        body, re.MULTILINE | re.DOTALL,
    )
    if not m:
        return ""
    return re.sub(r"<!--.*?-->", "", m.group(1), flags=re.DOTALL).strip()


def check_bottom_line(pages: dict[str, dict]) -> list[str]:
    """Content synthesis/initiative/segment pages open with a position statement."""
    out: list[str] = []
    for slug, page in pages.items():
        if page["fm"].get("type") not in _BOTTOM_LINE_TYPES:
            continue
        tail = slug.rsplit("/", 1)[-1]
        if tail in _NAV_SLUGS or tail == "catalog" or slug.startswith("log/"):
            continue
        if len(_section_content(page["body"], "Bottom line")) < _SECTION_MIN_CHARS:
            out.append(
                f"{page['path']}: missing or thin '## Bottom line' section — "
                f"open with a 2-4 sentence position statement (what we believe and why it matters)"
            )
    return out


def check_overview(pages: dict[str, dict]) -> list[str]:
    """overview.md must state current theses and must not be a bare link list."""
    page = pages.get("overview")
    if not page:
        return []
    out: list[str] = []
    if len(_section_content(page["body"], "Current theses")) < _SECTION_MIN_CHARS:
        out.append(
            "overview.md: '## Current theses' missing or empty — the overview must "
            "state what the project currently believes"
        )
    content_lines = [
        l.strip() for l in page["body"].splitlines()
        if l.strip() and not l.strip().startswith(("#", "<!--", "> "))
    ]
    if content_lines and all(
        l.startswith(("-", "*")) and ("[[" in l or "](" in l) for l in content_lines
    ):
        out.append(
            "overview.md: body is a link list, not an overview — add narrative "
            "synthesis (theses, what changed, top entities)"
        )
    return out


def _paragraphs(body: str):
    """Yield (first_line_no, text) for prose paragraphs.

    Skips fenced code blocks, headings, table rows, and the trust line
    ('> Trust: ...'). The trust line is lint-generated output, not user prose
    — including it would let lint's own numeric summary (e.g. "120 verified")
    trip the untagged-claim gate on the next run, an unrecoverable
    self-inflicted failure. Paragraphs are blocks of consecutive non-skipped
    lines separated by blank lines.
    """
    paras: list[tuple[int, str]] = []
    cur: list[str] = []
    start: int | None = None
    in_fence = False
    for n, line in enumerate(body.splitlines(), 1):
        s = line.strip()
        if s.startswith("```"):
            in_fence = not in_fence
            continue
        if in_fence or s.startswith("#") or s.startswith("|") or s.startswith("> Trust:"):
            continue
        if not s:
            if cur:
                paras.append((start, "\n".join(cur)))
                cur, start = [], None
            continue
        if start is None:
            start = n
        cur.append(line)
    if cur:
        paras.append((start, "\n".join(cur)))
    return paras


def find_untagged_claims(
    body: str, page_path: str, ptype: str, line_offset: int = 0
) -> list[str]:
    """Return one finding per uncovered paragraph containing a numeric claim.

    `line_offset` shifts reported line numbers from body-relative to
    file-relative (the number of lines consumed by the frontmatter block),
    so findings point at the actual line in the file rather than into the
    body alone.
    """
    if ptype in _CLAIM_EXEMPT_TYPES:
        return []
    findings: list[str] = []
    for line_no, para in _paragraphs(body):
        if CLAIM_COVERAGE_RE.search(para):
            continue
        scrubbed = re.sub(r"`[^`]*`", "", para)   # inline code is not prose
        for pat in _CLAIM_ALLOWLIST:
            scrubbed = pat.sub("", scrubbed)
        m = CLAIM_NUM_RE.search(scrubbed)
        if m:
            findings.append(
                f"{page_path}: line {line_no + line_offset}: untagged numeric claim '{m.group(0).strip()}' "
                f"— add a source tag, [[wikilink]], URL, or %%[no-claim]%%"
            )
    return findings


def validate_frontmatter(
    fm: dict, page_path: str, allowed_types: set[str] | None = None
) -> list[str]:
    """Return a list of error strings. Empty list = valid.

    allowed_types defaults to the global ALLOWED_TYPES (builtin set) when not
    supplied — preserved for callers that don't have wiki_root context (e.g.
    --check-content mode). full_lint() always passes the wiki-specific set.
    """
    if allowed_types is None:
        allowed_types = ALLOWED_TYPES
    errors: list[str] = []
    for field in REQUIRED_FIELDS:
        if field not in fm:
            errors.append(f"{page_path}: missing required field '{field}'")
    if fm.get("type") and fm["type"] not in allowed_types:
        errors.append(f"{page_path}: invalid type '{fm['type']}'")
    if fm.get("type") == "question":
        # Question pages use their own status vocabulary; skip generic status check
        q_status = fm.get("status", "")
        if q_status and q_status not in ALLOWED_QUESTION_STATUSES:
            errors.append(
                f"{page_path}: invalid status '{q_status}' for question type "
                f"(allowed: {', '.join(sorted(ALLOWED_QUESTION_STATUSES))})"
            )
    elif fm.get("status") and fm["status"] not in ALLOWED_STATUSES:
        errors.append(f"{page_path}: invalid status '{fm['status']}'")
    if fm.get("confidence") and fm["confidence"] not in ALLOWED_CONFIDENCE:
        errors.append(f"{page_path}: invalid confidence '{fm['confidence']}'")
    # Date format validation — prevents sentinel values like "TBD" from slipping past.
    # `is_stale` silently returns False for unparseable dates, so without this check
    # a template-copied page with `last-updated: TBD` would never be flagged as stale.
    # Templates themselves live in _templates/ (not under wiki/) so they're not scanned.
    lu = fm.get("last-updated")
    if lu and not DATE_RE.match(str(lu)):
        errors.append(f"{page_path}: invalid last-updated '{lu}' (expected YYYY-MM-DD)")
    q = fm.get("quarter")
    if q and not QUARTER_RE.match(str(q)):
        errors.append(f"{page_path}: invalid quarter '{q}' (expected YYYY-QN)")
    return errors


def check_author(fm: dict, page_path: str) -> list[str]:
    """session and decision pages must attribute their content: non-empty author list."""
    if fm.get("type") not in ("session", "decision"):
        return []
    author = fm.get("author")
    if isinstance(author, str):
        author = [author] if author.strip() and author.strip() != '""' else []
    if not author:
        return [
            f"{page_path}: type '{fm.get('type')}' requires non-empty 'author' list "
            f"(who produced/decided this)"
        ]
    return []


def check_confidence_vs_sources(fm: dict, page_path: str) -> list[str]:
    """Entity pages with zero sources may declare at most confidence: low.

    synthesis (navigational) and question (inherently unsourced) are exempt.
    """
    if fm.get("type") in ("synthesis", "question"):
        return []
    sources = fm.get("sources") or []
    if isinstance(sources, str):
        sources = [sources] if sources.strip() and sources.strip() != '""' else []
    sources = [s for s in sources if str(s).strip().strip('"').strip("'")]
    conf = fm.get("confidence", "")
    if not sources and conf in ("high", "med"):
        return [
            f"{page_path}: sources is empty but confidence is '{conf}' — "
            f"source-less pages are capped at confidence: low"
        ]
    return []


_DIGEST_BADNAME_RE = re.compile(r"(-v\d+(?:\.\d+)*$)|(\bsession\b)", re.IGNORECASE)


def check_digest_names(pages: dict[str, dict]) -> list[str]:
    """Digests must be topic-named, not session/version-named (AI Factory rule #4)."""
    out: list[str] = []
    for slug, page in pages.items():
        if page["fm"].get("type") != "source" or "digests/" not in slug:
            continue
        tail = slug.rsplit("/", 1)[-1]
        if tail == "catalog":
            continue
        if _DIGEST_BADNAME_RE.search(tail):
            out.append(
                f"{page['path']}: digest name '{tail}' looks session/version-based — "
                f"rename to a topic-based name"
            )
    return out


# Root files that are infrastructure, never strays.
_STRAY_ALLOWED_ROOT = {"README.md", "CLAUDE.md", "AGENTS.md", "GEMINI.md"}


def find_stray_files(wiki_root: Path) -> list[str]:
    """Top-level *.md files outside wiki/ or raw/ are ingest-pending strays.

    raw/ is ALWAYS a correct drop location (zero ceremony) and is never scanned.
    """
    return sorted(
        md.name for md in wiki_root.glob("*.md")
        if md.name not in _STRAY_ALLOWED_ROOT
    )


def find_dead_refs(pages: dict[str, dict]) -> list[str]:
    """sources:/related: entries that resolve to no page (by slug or tail).

    Skipped: raw/ paths, relative/absolute paths, URLs — those are checked by
    validate_external_refs or are legitimately outside the wiki page set.
    """
    known: set[str] = set()
    for slug in pages:
        known.add(slug)
        known.add(slug.rsplit("/", 1)[-1])
    out: list[str] = []
    for slug, page in pages.items():
        for field in ("sources", "related"):
            refs = page["fm"].get(field) or []
            if isinstance(refs, str):
                refs = [refs] if refs.strip() and refs.strip() != '""' else []
            for ref in refs:
                r = _unwrap_item(str(ref)).strip()
                if not r or r.startswith(("raw/", "../", "~", "/")) or "://" in r:
                    continue
                if r not in known and r.rsplit("/", 1)[-1] not in known:
                    out.append(f"{page['path']}: dead {field} ref '{r}' — no such page")
    return sorted(out)


from datetime import date, timedelta

STALE_AFTER_DAYS = 90


def _current_quarter() -> str:
    """Return current quarter in YYYY-QN format."""
    today = date.today()
    q = (today.month - 1) // 3 + 1
    return f"{today.year}-Q{q}"


def is_stale(fm: dict, today: date | None = None) -> bool:
    """True if status=active AND last-updated is more than STALE_AFTER_DAYS old."""
    if fm.get("status") != "active":
        return False
    raw = fm.get("last-updated")
    if not raw:
        return False
    try:
        y, m, d = map(int, str(raw).split("-"))
        last = date(y, m, d)
    except (ValueError, AttributeError):
        return False
    today = today or date.today()
    return (today - last) > timedelta(days=STALE_AFTER_DAYS)


def _catalog_prefix(fm: dict, open_questions: int, today: date) -> str:
    """Return ✓, △, or ? prefix for a §Agent Catalog entry.

    ✓ = confidence high|med, not stale, zero open questions
    △ = stale (>90d active), confidence low, or has open questions
    """
    if not fm:
        return "?"
    confidence = fm.get("confidence", "")
    tags = fm.get("tags") or []
    if is_stale(fm, today) or confidence == "low" or "unverified" in tags or open_questions > 0:
        return "△"
    return "✓"


def generate_start_here(pages: dict[str, dict]) -> str:
    """A 5-link reading path for humans: the index's front door."""
    items: list[str] = []
    if "overview" in pages:
        items.append("[[overview]] — current synthesis")

    def _newest(ptype: str, by_tail: bool):
        cands = [(s, p) for s, p in pages.items() if p["fm"].get("type") == ptype]
        if ptype == "source":
            cands = [
                (s, p) for s, p in cands
                if "digests/" in s and s.rsplit("/", 1)[-1] != "catalog"
            ]
        if not cands:
            return None
        if by_tail:
            cands.sort(key=lambda sp: sp[0].rsplit("/", 1)[-1], reverse=True)
        else:
            cands.sort(key=lambda sp: sp[1]["fm"].get("last-updated", ""), reverse=True)
        return cands[0][0].rsplit("/", 1)[-1]

    d = _newest("decision", by_tail=True)   # date-prefixed filenames sort chronologically
    if d:
        items.append(f"[[{d}]] — newest decision")
    open_qs = [
        (s, p) for s, p in pages.items()
        if p["fm"].get("type") == "question" and p["fm"].get("status") == "open"
    ]
    if open_qs:
        open_qs.sort(key=lambda sp: sp[1]["fm"].get("last-updated", ""), reverse=True)
        items.append(f"[[{open_qs[0][0].rsplit('/', 1)[-1]}]] — top open question")
    s = _newest("session", by_tail=True)
    if s:
        items.append(f"[[{s}]] — latest session")
    dg = _newest("source", by_tail=False)
    if dg:
        items.append(f"[[{dg}]] — newest digest")

    lines = ["## Start here"]
    for i, item in enumerate(items, 1):
        lines.append(f"{i}. {item}")
    return "\n".join(lines)


def generate_agent_catalog(pages: dict[str, dict], today: date | None = None) -> str:
    """Build the §Agent Catalog markdown block from page frontmatter and summaries.

    Groups entity pages by type (sorted newest first within each group).
    Question pages get their own section at the end.
    Excludes synthesis and source types.
    Returns the block text (without the AUTO-GENERATED markers).
    """
    today = today or date.today()
    entity_pages: dict[str, dict] = {}
    question_pages: dict[str, dict] = {}
    session_pages: dict[str, dict] = {}
    deliverable_pages: dict[str, dict] = {}
    for slug, page in pages.items():
        fm = page.get("fm", {})
        ptype = fm.get("type", "")
        ext_ref = str(fm.get("external-ref", "")).strip('"').strip("'")
        if ptype == "source" and ext_ref:
            deliverable_pages[slug] = page
            continue
        if ptype in _CATALOG_EXCLUDED_TYPES:
            continue
        if ptype == "question":
            question_pages[slug] = page
        elif ptype == "session":
            session_pages[slug] = page
        elif ptype:
            entity_pages[slug] = page

    lines: list[str] = ["## Agent Catalog"]

    by_type: dict[str, list] = {}
    for slug, page in entity_pages.items():
        ptype = page["fm"].get("type", "unknown")
        by_type.setdefault(ptype, []).append((slug, page))

    for ptype in sorted(by_type):
        group = by_type[ptype]
        if ptype == "decision":
            group.sort(key=lambda sp: sp[0].rsplit("/", 1)[-1], reverse=True)
        else:
            group.sort(key=lambda sp: sp[1]["fm"].get("last-updated", ""), reverse=True)
        lines.append(f"\n### {ptype.capitalize()}s ({len(group)})")
        for slug, page in group:
            fm = page["fm"]
            open_qs = page.get("open_questions", 0)
            prefix = _catalog_prefix(fm, open_qs, today)
            tail = slug.rsplit("/", 1)[-1]
            summary = str(fm.get("summary", "")).strip('"').strip("'")
            tags = fm.get("tags") or []
            confidence = fm.get("confidence", "")
            tag_str = f" [{', '.join(tags)}]" if tags else ""
            t = page.get("trust") or {}
            if (t.get("verified", 0) + t.get("hypothesis", 0) + t.get("refuted", 0)) > 0:
                conf_str = f" trust:{t['verified']}✓/{t['hypothesis']}~/{t['refuted']}✗"
            else:
                conf_str = f" confidence:{confidence}" if confidence else ""
            qs_str = f" open-Qs:{open_qs}" if open_qs > 0 else ""
            summary_part = f" — {summary}" if summary else ""
            lines.append(f"{prefix} [[{tail}]]{summary_part}{tag_str}{conf_str}{qs_str}")

    if session_pages:
        lines.append(f"\n### Sessions ({len(session_pages)})")
        ordered = sorted(session_pages.items(), key=lambda sp: sp[0].rsplit("/", 1)[-1], reverse=True)
        for slug, page in ordered:
            fm = page["fm"]
            tail = slug.rsplit("/", 1)[-1]
            summary = str(fm.get("summary", "")).strip('"').strip("'")
            summary_part = f" — {summary}" if summary else ""
            lines.append(f"- [[{tail}]]{summary_part}")

    if deliverable_pages:
        lines.append(f"\n### External deliverables ({len(deliverable_pages)})")
        ordered = sorted(
            deliverable_pages.items(),
            key=lambda sp: sp[1]["fm"].get("last-updated", ""), reverse=True,
        )
        for slug, page in ordered:
            fm = page["fm"]
            tail = slug.rsplit("/", 1)[-1]
            summary = str(fm.get("summary", "")).strip('"').strip("'")
            ext = str(fm.get("external-ref", "")).strip('"').strip("'")
            summary_part = f" — {summary}" if summary else ""
            lines.append(f"📎 [[{tail}]]{summary_part} ({ext})")

    if question_pages:
        open_count = sum(1 for p in question_pages.values() if p["fm"].get("status") == "open")
        partial_count = sum(1 for p in question_pages.values() if p["fm"].get("status") in ("partial", "blocked-internal"))
        resolved_count = sum(1 for p in question_pages.values() if p["fm"].get("status") == "resolved")
        lines.append(f"\n### Open Questions ({open_count} open, {partial_count} partial, {resolved_count} resolved)")
        for slug, page in sorted(question_pages.items(), key=lambda sp: sp[1]["fm"].get("last-updated", ""), reverse=True):
            fm = page["fm"]
            status = fm.get("status", "open")
            open_qs = page.get("open_questions", 0)
            if status == "open":
                prefix = "?"
            elif status == "resolved":
                prefix = "✓"
            else:
                prefix = "△"
            tail = slug.rsplit("/", 1)[-1]
            summary = str(fm.get("summary", "")).strip('"').strip("'")
            tags = fm.get("tags") or []
            tag_str = f" [{', '.join(tags)}]" if tags else ""
            summary_part = f" — {summary}" if summary else ""
            lines.append(f"{prefix} [[{tail}]]{summary_part}{tag_str} {status}")

    return "\n".join(lines)


def update_index_catalog(wiki_root: Path, catalog_text: str) -> None:
    """Write the §Agent Catalog block into wiki/index.md between AUTO-GENERATED markers.

    If markers don't exist yet, inserts after the YAML frontmatter closing ---.
    Preserves all other content.
    """
    index_path = wiki_root / "wiki" / "index.md"
    if not index_path.exists():
        return
    content = index_path.read_text()
    new_block = f"{CATALOG_AUTO_START}\n{catalog_text}\n{CATALOG_AUTO_END}"
    pattern = re.compile(
        re.escape(CATALOG_AUTO_START) + r".*?" + re.escape(CATALOG_AUTO_END),
        re.DOTALL,
    )
    if pattern.search(content):
        index_path.write_text(pattern.sub(new_block, content))
        return
    # No markers — insert after closing frontmatter ---
    lines = content.split("\n")
    if lines and lines[0].strip() == "---":
        close = next(
            (i for i, ln in enumerate(lines[1:], 1) if ln.strip() == "---"), None
        )
        if close is not None:
            lines.insert(close + 1, "")
            lines.insert(close + 1, CATALOG_AUTO_END)
            lines.insert(close + 1, catalog_text)
            lines.insert(close + 1, CATALOG_AUTO_START)
            index_path.write_text("\n".join(lines))
            return
    index_path.write_text(new_block + "\n\n" + content)


