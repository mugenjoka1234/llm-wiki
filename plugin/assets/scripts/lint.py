def check_recommended_fields(fm: dict, page_path: str) -> list[str]:
    """Return warning strings for missing or inadequate recommended fields.

    Synthesis pages are exempt — index, overview, glossary etc. don't need summaries.
    """
    if fm.get("type") == "synthesis":
        return []
    warnings: list[str] = []
    summary = fm.get("summary", "")
    if not summary:
        warnings.append(
            f"{page_path}: missing recommended field 'summary' "
            f"(add a one-line answer-preview: what question does this page answer?)"
        )
    elif len(str(summary).strip('"').strip("'")) < SUMMARY_MIN_LEN:
        warnings.append(
            f"{page_path}: summary too short ({len(str(summary).strip())} chars, "
            f"minimum {SUMMARY_MIN_LEN}) — be specific about what this page answers"
        )
    return warnings

_BUILTIN_TYPES = {
    "competitor", "initiative", "jtbd", "feature", "segment",
    "experiment", "metric", "decision", "source", "synthesis", "question",
    "session",
}

# ALLOWED_TYPES is populated at lint time from the wiki's CLAUDE.md.
# See load_allowed_types() — fallback is _BUILTIN_TYPES if CLAUDE.md is absent.
ALLOWED_TYPES = _BUILTIN_TYPES


def load_schema(wiki_root: Path) -> dict:
    """Parse schema.yaml if present. Returns dict with optional keys:
    - 'entity_types': set of valid type strings (always includes 'synthesis' and 'question')
    - 'domain': string domain name

    Returns empty dict if schema.yaml absent or unparseable.
    """
    schema_path = wiki_root / "schema.yaml"
    if not schema_path.exists():
        return {}
    text = schema_path.read_text()
    result: dict = {}
    entity_types: list = []
    in_entity_types = False
    for line in text.splitlines():
        stripped = line.strip()
        if stripped.startswith("domain:"):
            result["domain"] = stripped[len("domain:"):].strip()
        elif stripped == "entity_types:":
            in_entity_types = True
        elif in_entity_types and stripped.startswith("- "):
            type_name = stripped[2:].strip()
            if type_name and " " not in type_name:
                entity_types.append(type_name)
        elif in_entity_types and not stripped.startswith("- ") and stripped:
            in_entity_types = False
    if entity_types:
        types = set(entity_types)
        types.add("synthesis")   # always valid
        types.add("question")    # always valid (added in Approach B)
        types.add("session")     # always valid (merge Phase 2)
        result["entity_types"] = types
    return result


def load_allowed_types(wiki_root: Path) -> set[str]:
    """Read entity types from the wiki's CLAUDE.md ## Entity types section.

    Handles two formats produced by different generations of the wiki template:

    Bullet list (new wikis):
        ## Entity types
        - tool
        - comparison

    Markdown table (legacy wiki format):
        ## Entity types
        | type | template | purpose |
        |---|---|---|
        | competitor | ... | ... |

    Falls back to _BUILTIN_TYPES if CLAUDE.md is absent or has no parseable
    ## Entity types section — keeps the tool working on legacy wikis.
    """
    schema = load_schema(wiki_root)
    if "entity_types" in schema:
        return schema["entity_types"]

    claude_md = wiki_root / "CLAUDE.md"
    if not claude_md.exists():
        return set(_BUILTIN_TYPES)

    text = claude_md.read_text()
    section_re = re.compile(
        r"^## Entity types\s*\n(.*?)(?=^## |\Z)", re.MULTILINE | re.DOTALL
    )
    m = section_re.search(text)
    if not m:
        return set(_BUILTIN_TYPES)

    types: set[str] = set()
    for line in m.group(1).splitlines():
        stripped = line.strip()
        # Bullet list format: "- type-name"
        if stripped.startswith("- ") and not stripped.startswith("- **"):
            type_name = stripped[2:].strip()
            if type_name and " " not in type_name:  # single-word type names only
                types.add(type_name)
        # Markdown table format: "| type-name | ..." — skip header/separator rows
        elif stripped.startswith("|") and not stripped.startswith("|---") and not stripped.startswith("| type"):
            first_col = stripped.split("|")[1].strip()
            # Strip backtick formatting if present, skip empty/header cells
            first_col = first_col.strip("`").strip()
            if first_col and " " not in first_col and first_col != "---":
                types.add(first_col)

    # Always include synthesis and question — reserved built-in types
    types.add("synthesis")
    types.add("question")
    types.add("session")
    return types if types else set(_BUILTIN_TYPES)

