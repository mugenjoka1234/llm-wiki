# plugin/tests/integration/test_agent_catalog.py
"""Tests for §Agent Catalog generation in index.md."""
from datetime import date, timedelta
import pytest
from pathlib import Path
from conftest import run_lint, _write_synthesis_page

TODAY = date.today().isoformat()
STALE_DATE = (date.today() - timedelta(days=100)).isoformat()

AUTO_START = "<!-- AUTO-GENERATED by lint.py — do not edit this section -->"
AUTO_END = "<!-- END AUTO-GENERATED -->"


def _entity(type_name, confidence="high", last_updated=None, summary=None, open_qs=0,
            status="active", extra=""):
    lu = last_updated or TODAY
    sq = "".join(f"- [ ] Open question {i}\n" for i in range(open_qs))
    s = f'summary: "{summary}"\n' if summary else ""
    return (
        f"---\ntype: {type_name}\nstatus: {status}\nlast-updated: {lu}\n"
        f"quarter: 2026-Q2\nokr: []\nconfidence: {confidence}\n"
        f"sources: []\nrelated: []\ntags: []\n{s}{extra}---\n# Page\n\n"
        f"## Open questions\n{sq}"
    )


def test_catalog_inserted_into_index_after_lint(wiki):
    """After lint runs, index.md contains the AUTO-GENERATED catalog block."""
    (wiki / "wiki/shopify-competitor.md").write_text(
        _entity("competitor", summary="Shopify: 70+ reports all plans; no Z report; Sidekick AI")
    )
    run_lint(wiki)
    index_content = (wiki / "wiki/index.md").read_text()
    assert AUTO_START in index_content
    assert "## Agent Catalog" in index_content
    assert "[[shopify-competitor]]" in index_content


def test_catalog_check_prefix(wiki):
    """High-confidence, current, no open-Qs entity gets ✓ prefix."""
    (wiki / "wiki/shopify-competitor.md").write_text(
        _entity("competitor", confidence="high",
                summary="Shopify: 70+ reports all plans; no Z report capability confirmed")
    )
    run_lint(wiki)
    index = (wiki / "wiki/index.md").read_text()
    assert "✓ [[shopify-competitor]]" in index


def test_catalog_delta_prefix_stale(wiki):
    """Stale entity (>90 days old, status:active) gets △ prefix."""
    (wiki / "wiki/old-competitor.md").write_text(
        _entity("competitor", last_updated=STALE_DATE,
                summary="Old Competitor: legacy product with limited reporting features confirmed")
    )
    run_lint(wiki)
    index = (wiki / "wiki/index.md").read_text()
    assert "△ [[old-competitor]]" in index


def test_catalog_delta_prefix_open_questions(wiki):
    """Entity with open questions gets △ prefix regardless of confidence."""
    (wiki / "wiki/shopify-competitor.md").write_text(
        _entity("competitor", confidence="high", open_qs=2,
                summary="Shopify: 70+ reports all plans; no Z report; Sidekick AI present now")
    )
    run_lint(wiki)
    index = (wiki / "wiki/index.md").read_text()
    assert "△ [[shopify-competitor]]" in index


def test_catalog_regenerates_on_second_run(wiki):
    """Running lint twice replaces the catalog block, not appends."""
    (wiki / "wiki/shopify-competitor.md").write_text(
        _entity("competitor",
                summary="Shopify: 70+ reports all plans; no Z report; Sidekick AI confirmed")
    )
    run_lint(wiki)
    run_lint(wiki)
    index = (wiki / "wiki/index.md").read_text()
    assert index.count(AUTO_START) == 1, "Catalog block must appear exactly once"


def test_catalog_preserves_obsidian_section(wiki):
    """Human-authored content in index.md after the AUTO-GENERATED block is preserved."""
    index_path = wiki / "wiki/index.md"
    existing = index_path.read_text()
    index_path.write_text(existing + "\n## Obsidian Navigation\n\nSome Dataview query here.\n")
    run_lint(wiki)
    new_content = index_path.read_text()
    assert "Obsidian Navigation" in new_content
    assert "Dataview query here" in new_content


def test_synthesis_pages_not_in_catalog(wiki):
    """Synthesis-type pages (index, overview, _health) do not appear in catalog entity sections."""
    run_lint(wiki)
    index = (wiki / "wiki/index.md").read_text()
    start = index.find(AUTO_START)
    end = index.find(AUTO_END)
    if start >= 0 and end >= 0:
        block = index[start:end]
        catalog_start = block.find("## Agent Catalog")
        if catalog_start >= 0:
            block = block[catalog_start:]
        assert "[[overview]]" not in block
        assert "[[_health]]" not in block


