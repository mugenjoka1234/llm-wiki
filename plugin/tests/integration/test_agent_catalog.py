def test_digests_catalog_created(wiki):
    """lint.py creates wiki/digests/catalog.md listing all digests."""
    digest_path = wiki / "wiki/digests/research-shopify-2026-05-07.md"
    digest_path.write_text(
        "---\n"
        "type: source\n"
        "status: active\n"
        f"last-updated: {TODAY}\n"
        "quarter: 2026-Q2\n"
        "okr: []\n"
        "confidence: high\n"
        "sources: []\n"
        "related: []\n"
        "tags: [competitive]\n"
        'summary: "Shopify reporting research: plan availability, ShopifyQL, no Z report confirmed"\n'
        "---\n"
        "# Research — Shopify Reporting\n"
    )
    run_lint(wiki)
    catalog = wiki / "wiki/digests/catalog.md"
    assert catalog.exists(), "digests/catalog.md should be created by lint"
    content = catalog.read_text()
    assert "research-shopify-2026-05-07" in content
    assert "Shopify reporting research" in content


def test_digests_catalog_sorted_newest_first(wiki):
    """Digests catalog is sorted by last-updated descending."""
    from datetime import timedelta
    older = (date.today() - timedelta(days=60)).isoformat()
    for slug, date_str in [("research-a", older), ("research-b", TODAY)]:
        (wiki / f"wiki/digests/{slug}.md").write_text(
            f"---\ntype: source\nstatus: active\nlast-updated: {date_str}\n"
            "quarter: 2026-Q2\nokr: []\nconfidence: high\nsources: []\nrelated: []\n"
            f'tags: []\nsummary: "{slug} summary line that is long enough to pass"\n---\n# R\n'
        )
    run_lint(wiki)
    content = (wiki / "wiki/digests/catalog.md").read_text()
    pos_a = content.find("research-a")
    pos_b = content.find("research-b")
    assert pos_b < pos_a, "Newer digest (research-b) should appear before older (research-a)"


# ── Task 6: questions/catalog.md ─────────────────────────────────────────────

def _question_page(slug_name: str, status: str = "open", open_qs: int = 2,
                   summary: str = None) -> str:
    s = summary or f"{slug_name}: strategic question about domain requiring research to answer"
    return (
        "---\n"
        "type: question\n"
        f"status: {status}\n"
        f"last-updated: {TODAY}\n"
        "quarter: 2026-Q2\n"
        "okr: []\n"
        "confidence: low\n"
        "sources: []\n"
        "related: []\n"
        "tags: []\n"
        f'summary: "{s}"\n'
        "---\n"
        f"# {slug_name}\n\n"
        "## Sub-questions\n"
        + "".join(f"- [ ] Sub-question {i}\n" for i in range(open_qs))
    )


def test_question_type_valid(wiki):
    """type: question is a valid type even without schema.yaml."""
    questions_dir = wiki / "wiki/questions"
    questions_dir.mkdir()
    (questions_dir / "q-test.md").write_text(_question_page("q-test"))
    code, out, err = run_lint(wiki)
    assert "invalid type 'question'" not in err, f"question type rejected: {err}"


def test_invalid_question_status_is_schema_error(wiki):
    """A question page with an unrecognized status value is a schema error."""
    questions_dir = wiki / "wiki/questions"
    questions_dir.mkdir()
    bad = _question_page("q-bad").replace("status: open", "status: in-progress")
    (questions_dir / "q-bad.md").write_text(bad)
    code, out, err = run_lint(wiki)
    assert code == 2, f"Bad question status should be schema error, got {code}"
    assert "invalid status" in err


def test_questions_catalog_created(wiki):
    """lint.py creates wiki/questions/catalog.md when questions/ folder exists."""
    questions_dir = wiki / "wiki/questions"
    questions_dir.mkdir()
    (questions_dir / "q-cash-management.md").write_text(
        _question_page("q-cash-management", status="open", open_qs=3,
                       summary="Cash management F13: what the product needs for Z report; 3 sub-questions open")
    )
    run_lint(wiki)
    catalog = questions_dir / "catalog.md"
    assert catalog.exists()
    content = catalog.read_text()
    assert "q-cash-management" in content
    assert "?" in content  # open question gets ? prefix


def test_questions_catalog_not_created_without_folder(wiki):
    """If questions/ folder doesn't exist, catalog.md is not created."""
    run_lint(wiki)
    assert not (wiki / "wiki/questions/catalog.md").exists()
